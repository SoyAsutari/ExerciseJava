class MethodDemon2{
public static void main(String args[]){

	int no1 = 3, no2 = 10, hasil;
	hasil = jumlah (no1, no2);



	System.out.println("Number 1 = "+no1);
	System.out.println("Number 2 = "+no2);
	System.out.println("The Total = "+hasil);
}

	static int jumlah(int no1 , int no2){

		int total;
		total = no1 + no2;
		return total;
	}
}
 